#creacion de operadores aritmeticoa

a = 15
b = 1.2

#operaciones aritmeticas 
suma = a + b
resta = a - b
multiplicacion = a * b 
division = a / b
division_entera = a // b
exponente = a ** b

print("suma", suma)
print("resta", resta)
print("multiplicacion", multiplicacion)
print("division", division)
print("division entera", division_entera)
print("potencia", round(exponente, 0))